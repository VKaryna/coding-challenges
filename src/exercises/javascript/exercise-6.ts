/**
 * Create a function that removes all duplicate elements from an array of numbers and strings.
 *
 * Note: If a duplicate exists as both a number and a string,
 * the resulting array should retain the number.
 *
 * Input: [0, "hello", 2, 42, "2", 4, 6, 8, 6]
 * Output: [0, "hello", 2, 42, 4, 6, 8]
 */

export const exercise6 = (input: (number | string)[]): (number | string)[] => {
  const result = input.map((element) => {
    return typeof element === "string" && !isNaN(Number(element))
      ? Number(element)
      : element;
  });

  return Array.from(new Set(result));
};
